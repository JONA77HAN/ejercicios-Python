# Al igual que con listas, podes utilizar los operadores in y not in para verificar si una clave o valor 
# existe dentro de un diccionario en particular.  
spam = {'nombre': 'Sofía', 'edad': 7} 
print('nombre' in spam.keys() )
'Sofía' in spam.values() 
'color' in spam.keys() 
'color' not in spam.keys() 
'color' in spam 

print(7 in spam.keys())
  
